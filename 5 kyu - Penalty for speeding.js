function penalty(a_list) {
    return a_list.sort((a, b) => a + b > b + a ? 1 : -1).join('');
}

penalty(['45', '30', '50', '1'])//, '1304550')
penalty(['100', '10', '1'])//, '100101')
penalty(['32', '3'])//, '323')
penalty(['70', '46', '4', '19'])//, '1944670')
